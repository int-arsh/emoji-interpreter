// Export a function to get a random emoji from the database
export const getRandomEmoji = () => {
  const emojiKeys = Object.keys(emojiDatabase)
  return emojiKeys[Math.floor(Math.random() * emojiKeys.length)]
}

// Export a function to get emoji count
export const getEmojiCount = () => {
  return Object.keys(emojiDatabase).length
}

// Export a function to search emojis by keyword
export const searchEmojis = (keyword) => {
  const results = []
  const searchTerm = keyword.toLowerCase()
  
  for (const [emoji, meaning] of Object.entries(emojiDatabase)) {
    if (meaning.toLowerCase().includes(searchTerm)) {
      results.push({ emoji, meaning })
    }
  }
  
  return results
}

