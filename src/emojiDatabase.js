// Export a function to search emojis by keyword
export const searchEmojis = (keyword) => {
  const results = []
  const searchTerm = keyword.toLowerCase()
  
  for (const [emoji, meaning] of Object.entries(emojiDatabase)) {
    if (meaning.toLowerCase().includes(searchTerm)) {
      results.push({ emoji, meaning })
    }
  }
  
  return results
}

